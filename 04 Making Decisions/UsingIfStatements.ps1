# Use an if statment and print out if the values are above 5

$firstValue = 4
$secondValue = 7
$thirdValue = 10

if($firstValue -gt 5)
{
    Write-Host "The value $firstValue is above 5"
}

if($secondValue -gt 5)
{
    Write-Host "The value $secondValue is above 5"
}

if($thirdValue -gt 5)
{
    Write-Host "The value $thirdValue is above 5"
}